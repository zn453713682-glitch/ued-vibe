#!/usr/bin/env node
/**
 * 扫 ROOT 下的 {系统}/{版本}/*.html + *.md，输出 manifest.js
 * 目录约定:
 *   ued-vibe/
 *   ├── 灵翼/v1.0/foo.inline.html
 *   ├── 灵翼/v1.0/foo.md
 *   ├── 灵翼/v1.1/...
 *   └── 其它系统/v2.0/...
 *
 * 顶层散文件(老布局,如 index.html 同级的 .inline.html)兜底进 DEFAULT_BUCKET。
 * 用法: node build-manifest.mjs   (在 vibe-gallery 目录)
 */
import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(process.argv[2] || '.');
// 扫描时跳过的顶层目录(脚手架/不属于业务文件)
const SKIP_TOP = new Set(['.git', 'node_modules', '.github', '.vscode', '.idea']);
// 兜底分桶:顶层散落的 *.html / *.md 全归到这里
const DEFAULT_BUCKET = { system: '灵翼', version: 'v1.0' };

// ----- 工具 -----
const fmtSize = n => {
  if (n < 1024) return `${n}B`;
  if (n < 1024 * 1024) return `${(n / 1024).toFixed(1)}KB`;
  return `${(n / 1024 / 1024).toFixed(1)}MB`;
};
const fmtMtime = ms => {
  const d = new Date(ms);
  const pad = x => String(x).padStart(2, '0');
  return `${pad(d.getMonth() + 1)}-${pad(d.getDate())} ${pad(d.getHours())}:${pad(d.getMinutes())}`;
};
const stripExt = name => name.replace(/\.inline\.html$/, '').replace(/\.html$/, '');

/**
 * 在 dir 内寻找 .html 列表,并配对同名 .md(优先 stem.md,再 stem.inline.md)。
 * 返回 [{html, md, htmlBasename, mdBasename, ...}]
 */
function scanLeafDir(absDir, relDir) {
  const entries = fs.readdirSync(absDir, { withFileTypes: true });
  const htmls = entries.filter(e => e.isFile() && /\.html$/i.test(e.name));
  const mdSet = new Set(entries.filter(e => e.isFile() && /\.md$/i.test(e.name)).map(e => e.name));
  return htmls.map(h => {
    const stem = stripExt(h.name);
    const mdCandidate = [`${stem}.md`, `${stem}.inline.md`].find(c => mdSet.has(c));
    const stat = fs.statSync(path.join(absDir, h.name));
    const htmlRel = relDir === '.' ? h.name : `${relDir}/${h.name}`;
    const mdRel = mdCandidate ? (relDir === '.' ? mdCandidate : `${relDir}/${mdCandidate}`) : null;
    return {
      name: h.name,
      displayName: stem,
      html: htmlRel,
      htmlBasename: h.name,
      md: mdRel,
      mdBasename: mdCandidate || `${stem}.md`,
      dir: relDir,
      size: stat.size,
      sizeStr: fmtSize(stat.size),
      mtime: stat.mtimeMs / 1000,
      mtimeStr: fmtMtime(stat.mtimeMs),
      group: '',
      inline: /\.inline\.html$/i.test(h.name),
    };
  });
}

// ----- 主扫描 -----
const items = [];
const tree = {}; // {system: Set<version>}

function addBucket(system, version) {
  if (!tree[system]) tree[system] = new Set();
  tree[system].add(version);
}

function walkSystem(systemName, systemAbs) {
  // systemAbs 下每个子目录都视为 version
  const versions = fs.readdirSync(systemAbs, { withFileTypes: true })
    .filter(e => e.isDirectory());
  for (const v of versions) {
    const verAbs = path.join(systemAbs, v.name);
    const verRel = `${systemName}/${v.name}`;
    addBucket(systemName, v.name);
    const found = scanLeafDir(verAbs, verRel);
    found.forEach(it => items.push({ ...it, system: systemName, version: v.name }));
  }
}

const topEntries = fs.readdirSync(ROOT, { withFileTypes: true });

// 1) 顶层散文件 → DEFAULT_BUCKET
const topFiles = scanLeafDir(ROOT, '.').filter(it => it.htmlBasename !== 'index.html');
if (topFiles.length) {
  addBucket(DEFAULT_BUCKET.system, DEFAULT_BUCKET.version);
  topFiles.forEach(it => items.push({ ...it, system: DEFAULT_BUCKET.system, version: DEFAULT_BUCKET.version }));
}

// 2) 子目录 → 按 {系统}/{版本}/ 递归
for (const e of topEntries) {
  if (!e.isDirectory()) continue;
  if (SKIP_TOP.has(e.name)) continue;
  if (e.name.startsWith('.')) continue;
  walkSystem(e.name, path.join(ROOT, e.name));
}

// ----- 排序:每个 (system,version) 内按 mtime 降序;system / version 字典序 -----
items.sort((a, b) => b.mtime - a.mtime);

// 兜底:如果一个文件都没有,至少声明一个空版本,避免前端报错
if (Object.keys(tree).length === 0) addBucket(DEFAULT_BUCKET.system, DEFAULT_BUCKET.version);

const SYSTEMS = Object.keys(tree).sort((a, b) => a.localeCompare(b, 'zh'));
const VERSIONS_BY_SYSTEM = {};
SYSTEMS.forEach(s => {
  VERSIONS_BY_SYSTEM[s] = [...tree[s]].sort((a, b) => b.localeCompare(a, 'zh', { numeric: true }));
});

// ----- 写出 manifest.js -----
const out = `// AUTO-GENERATED by build-manifest.mjs — DO NOT EDIT BY HAND.
// 改动需求改 build-manifest.mjs,然后重跑 \`node build-manifest.mjs\`。
const MANIFEST = ${JSON.stringify(items, null, 2)};
const SYSTEMS = ${JSON.stringify(SYSTEMS)};
const VERSIONS_BY_SYSTEM = ${JSON.stringify(VERSIONS_BY_SYSTEM, null, 2)};
const DEFAULT_BUCKET = ${JSON.stringify(DEFAULT_BUCKET)};
`;
fs.writeFileSync(path.join(ROOT, 'manifest.js'), out, 'utf8');

console.log(`[build-manifest] wrote manifest.js`);
console.log(`  systems  : ${SYSTEMS.join(', ') || '(empty)'}`);
SYSTEMS.forEach(s => {
  const verCount = VERSIONS_BY_SYSTEM[s].length;
  const fileCount = items.filter(it => it.system === s).length;
  console.log(`  └─ ${s}: ${verCount} versions, ${fileCount} files`);
});
console.log(`  total    : ${items.length} files`);
